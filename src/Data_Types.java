
public class Data_Types {

	public static void main(String[] args) {
		
	
		// Data types
		
// PRIMITIVE => data type having predefined memory size
		
		// short 
		short s = 10;
		
		// byte
		byte by = 10;
		
		// int
		int  a =10;
		
		// float
		float b = 10.02f;
		
		
		// double
		double c = 70.80;
		
		// long 
		long l = 123456789010l;
		
		// boolean
		// true or false
		boolean f = true;
		
		
		
		// char
		char ch = 'A';
		
//	Non- Primitive
		
		// String
	// sequence of characters is string
		
		String ch1 = "You are doing great!";
		
		System.out.println(ch1);
		
		
		
		
	}

}
